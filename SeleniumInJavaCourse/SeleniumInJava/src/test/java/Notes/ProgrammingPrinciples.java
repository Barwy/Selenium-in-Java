/*DRY - Don't Repeat Yourself
Jego celem jest redukcja powtórzen w kodzie, co:
* ulatwia i przyspiesza jego utrzymanie;
* zwieksza czytelność kodu;

Osiaga sie to poprzez:
* normalizacje danych (by zmniejszyć zależności i ilość nadmiernego kodu);
* zamiane powtórzeń na abstrakcje (pętle, metody, klasy);
 */